const slugify = (s: string) =>
  s
    .toLowerCase()
    .normalize("NFD")
    .replace(/[^\w\s-]/g, "")
    .trim()
    .replace(/\s+/g, "-");

const buildImageUrl = (p: BaseProducto) => {
  // Función simplificada para URLs de imágenes sin necesidad de API keys
  // Esta función retorna una URL inmediatamente sin esperar a una API
  
  // Método 1: Intenta usar Fragrantica/PerfumesDB que tiene muchas imágenes con fondo transparente
  const normalizedName = p.nombre.toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]/g, "-");
  
  // Usa un diccionario de mapeo para perfumes más conocidos
  const knownPerfumeIds: Record<string, string> = {
    "aventus": "10555",
    "sauvage": "28842",
    "bleu-de-chanel": "9099",
    "one-million": "6983",
    "invictus": "19681",
    "la-vie-est-belle": "15096",
    "olympea": "30902",
    "baccarat-rouge-540": "46454", 
    "good-girl": "38112",
    "black-opium": "25324"
    // Puedes añadir más perfumes conocidos manualmente
  };
  
  // Busca coincidencias parciales en los nombres de perfumes conocidos
  const matchKey = Object.keys(knownPerfumeIds).find(key => 
    normalizedName.includes(key));
    
  if (matchKey) {
    return `https://fimgs.net/mdimg/perfume/375x500.${knownPerfumeIds[matchKey]}.jpg`;
  }
  
  // Método 2: Como respaldo, usa Unsplash
  return `https://source.unsplash.com/featured/?perfume,bottle,${encodeURIComponent(
    slugify(p.nombre)
  )}`;
};

