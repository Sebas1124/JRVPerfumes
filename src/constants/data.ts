/*
 * =====================================================================================
 * ESTRATEGIA DE PRECIOS Y MANEJO DE IMÁGENES
 * =====================================================================================
 *
 * 1. CÁLCULO DE PRECIOS DE VENTA (COP):
 * - Se ha tomado como base los precios de coste (ej: 12.5 y 8.5). Se asume que
 * estos costes están en una moneda de referencia (ej. USD).
 * - Se aplicó una tasa de conversión aproximada a pesos colombianos (COP).
 * (Ej: 1 USD ≈ 4,100 COP).
 * - Sobre el coste en COP, se ha aplicado un margen de ganancia comercial
 * (aproximadamente del 100%-120%) para obtener un precio de venta final
 * que sea rentable y atractivo para el cliente.
 * - Los precios finales se han redondeado a valores psicológicos (terminados en 900)
 * para mejorar la percepción del cliente.
 *
 * 2. MANEJO DE IMÁGENES SIN BACKEND:
 * - Se ha añadido el campo `imagenUrl` a cada producto.
 * - Para una SPA (Single Page Application) sin backend, la mejor estrategia es
 * alojar las imágenes en un servicio de almacenamiento de archivos estáticos.
 * Opciones populares y gratuitas (con límites generosos) son:
 * a) Firebase Storage
 * b) Cloudinary
 * c) Un repositorio público en GitHub.
 * - Una vez subas tus imágenes a uno de estos servicios, obtendrás una URL pública
 * para cada una, la cual debes colocar en el campo `imagenUrl` correspondiente.
 * - Como marcador de posición, he usado el servicio `placehold.co` que genera
 * imágenes temporales con el nombre del producto. Deberás reemplazar estas URLs
 * por las tuyas.
 *
 */

